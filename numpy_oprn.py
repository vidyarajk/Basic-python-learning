## vector operations
import numpy as np
arr1=np.array([1,2,3,4,5])
arr2=np.array([10,20,30,40,50])
print("addition:",arr1+arr2)
print("substraction:",arr1-arr2)
print("multiplication:",arr1*arr2)
print("division:",arr1/arr2)

print(np.log(arr1))
print(np.sqrt(arr1))
print(np.exp(arr1))
print(np.sin(arr1))
